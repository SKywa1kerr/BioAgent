const bundles: Record<AppLanguage, Dict> = { zh, en };

export function t(language: AppLanguage, key: string, params?: Record<string, string | number>) {
  const table = bundles[language] || en;
  const template = table[key] ?? en[key] ?? key;
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, p: string) => String(params[p] ?? `{${p}}`));
}






